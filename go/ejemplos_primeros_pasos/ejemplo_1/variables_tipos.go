package main

import "fmt"

func main() {
	// Declaración de variables
	var nombre string = "Go"
	var edad int = 15
	var pi float64 = 3.14159
	var esVerdad bool = true

	// Imprimir variables
	fmt.Println("Nombre:", nombre)
	fmt.Println("Edad:", edad)
	fmt.Println("Pi:", pi)
	fmt.Println("Es verdad:", esVerdad)

	// Declaración corta
	apellido := "Lenguaje"
	fmt.Println("Apellido:", apellido)
}